package Lec1;

public class Arithmetic {
    public static void main(String[] args) {
        int a = 9;
        int b = 6;
        int c = a + b;
        System.out.println(c);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(b--);
        System.out.println(b);
    }
}
